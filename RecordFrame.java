    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        salesModel.setRowCount(0); // Clear existing table rows

        // Load sales records from database and populate table
        try (Connection conn = DBConnection.connect(); PreparedStatement ps = conn.prepareStatement(
                "SELECT s.id, CONCAT(c.name, ' ', c.surname) AS customer, m.name AS medicine, sd.quantity, sd.quantity * m.price AS total "
                + "FROM sale s "
                + "JOIN customer c ON s.customer_id = c.id "
                + "JOIN sale_detail sd ON sd.sale_id = s.id "
                + "JOIN medicine m ON sd.medicine_id = m.id")) {

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id"),
                    rs.getString("customer"),
                    rs.getString("medicine"),
                    rs.getInt("quantity"),
                    rs.getDouble("total")
                };
                salesModel.addRow(row);
            }

        } catch (SQLException ex) {
            Logger.getLogger(RecordFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Failed to load records", "Error", JOptionPane.ERROR_MESSAGE);
        }
        jTable1.setModel(salesModel); // Update table with data
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        salesModel.setRowCount(0); // Clear existing table rows

        // Read sales records from sales.txt file and populate table
        try (BufferedReader reader = new BufferedReader(new FileReader("sales.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",", 6); // Split line into parts
                if (parts.length == 6) {
                    Object[] row = {
                        parts[0], // Sale ID
                        parts[1], // Customer Name
                        parts[2], // Medicine
                        parts[3], // Quantity
                        parts[4] + "." + parts[5] // Total price
                    };
                    salesModel.addRow(row);
                }
            }
            jTable1.setModel(salesModel); // Update table
        } catch (IOException ex) {
            Logger.getLogger(RecordFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error reading sales file!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Filter sales table by customer name or medicine name based on search type
        String type = cmbSearchType.getSelectedItem().toString();
        String keyword = jTextField1.getText().trim().toLowerCase();

        if (type.equals("ALL RECORDS")) {
            jTable1.setModel(salesModel);  // Show all data
            return;
        }

        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a search key.");
            return;
        }

        DefaultTableModel filtered = new DefaultTableModel(
                new String[]{"Sale ID", "Customer Name", "Medicine", "Quantity", "Total Price"}, 0
        );

        for (int i = 0; i < salesModel.getRowCount(); i++) {
            String customer = salesModel.getValueAt(i, 1).toString().toLowerCase();
            String medicine = salesModel.getValueAt(i, 2).toString().toLowerCase();

            boolean match = switch (type) {
                case "BY CUSTOMER NAME" ->
                    customer.contains(keyword);
                case "BY MEDICINE NAME" ->
                    medicine.contains(keyword);
                default ->
                    false;
            };

            if (match) {
                filtered.addRow(new Object[]{
                    salesModel.getValueAt(i, 0),
                    salesModel.getValueAt(i, 1),
                    salesModel.getValueAt(i, 2),
                    salesModel.getValueAt(i, 3),
                    salesModel.getValueAt(i, 4)
                });
            }
        }

        jTable1.setModel(filtered); // Show filtered results
        if (salesModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please click LIST ALL or READ FROM FILE before searching.");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

