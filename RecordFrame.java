    private void exportVisibleDataToFile() {
        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "There is no data to export!");
            return;
        }

        String filename = JOptionPane.showInputDialog(this, "Enter filename:", "exported_sales.txt");

        if (filename == null || filename.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Export cancelled. No filename provided.");
            return;
        }

        try (PrintWriter pw = new PrintWriter(filename)) {
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                for (int j = 0; j < jTable1.getColumnCount(); j++) {
                    pw.print(jTable1.getValueAt(i, j));
                    if (j < jTable1.getColumnCount() - 1) {
                        pw.print(",");
                    }
                }
                pw.println();
            }
            JOptionPane.showMessageDialog(this, "Data exported successfully to " + filename);
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Export failed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

